package com.rc.portal.webapp.model;


public class GoodsFormBeanModel2 {
	private String manufacturer="";//生产厂家
	private String dose="";//剂量
	private String brand="";//品牌
	private String promotion="";//促销
	private String defaultimg="";//默认图片
	private String defaultimgs="";//默认图片
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getDose() {
		return dose;
	}
	public void setDose(String dose) {
		this.dose = dose;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getPromotion() {
		return promotion;
	}
	public void setPromotion(String promotion) {
		this.promotion = promotion;
	}
	public String getDefaultimg() {
		return defaultimg;
	}
	public void setDefaultimg(String defaultimg) {
		this.defaultimg = defaultimg;
	}
	public String getDefaultimgs() {
		return defaultimgs;
	}
	public void setDefaultimgs(String defaultimgs) {
		this.defaultimgs = defaultimgs;
	}
	
}
